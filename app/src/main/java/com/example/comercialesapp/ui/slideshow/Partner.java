package com.example.comercialesapp.ui.slideshow;

public class Partner {
    private String empresa;
    private String nombre;
    private String apellido;
    private String apellido2;
    private String dni;
    private String ciudad;
    private String direccion1;
    private String direccion2;
    private String formaPago;

    public Partner(String empresa, String nombre, String apellido, String apellido2, String dni, String ciudad, String direccion1, String direccion2, String formaPago) {
        this.empresa = empresa;
        this.nombre = nombre;
        this.apellido = apellido;
        this.apellido2 = apellido2;
        this.dni = dni;
        this.ciudad = ciudad;
        this.direccion1 = direccion1;
        this.direccion2 = direccion2;
        this.formaPago = formaPago;
    }

}
